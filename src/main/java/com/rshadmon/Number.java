package com.rshadmon;

public class Number {
    private int num;

    private int delay;

    public Number() {
        this.num = 0;
        this.delay = 0;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getDelay() {
        return delay;
    }

    public void setDelay(int delay) {
        this.delay = delay;
    }
}